package es.golemdr.tragaldaba.form;

public class TragaldabaForm {

    private String dniCliente;
    private String idsPlatos;
    private String datosPedido;

    public String getDniCliente() {
        return dniCliente;
    }

    public void setDniCliente(String dniCliente) {
        this.dniCliente = dniCliente;
    }

    public String getIdsPlatos() {
        return idsPlatos;
    }

    public void setIdsPlatos(String idsPlatos) {
        this.idsPlatos = idsPlatos;
    }

    public String getDatosPedido() {
        return datosPedido;
    }

    public void setDatosPedido(String datosPedido) {
        this.datosPedido = datosPedido;
    }
}
